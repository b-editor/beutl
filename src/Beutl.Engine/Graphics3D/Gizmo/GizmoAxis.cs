namespace Beutl.Graphics3D.Gizmo;

/// <summary>
/// Represents which axis of the gizmo is selected.
/// </summary>
public enum GizmoAxis
{
    /// <summary>
    /// No axis selected.
    /// </summary>
    None = 0,

    /// <summary>
    /// X axis (red).
    /// </summary>
    X = 1,

    /// <summary>
    /// Y axis (green).
    /// </summary>
    Y = 2,

    /// <summary>
    /// Z axis (blue).
    /// </summary>
    Z = 3
}
