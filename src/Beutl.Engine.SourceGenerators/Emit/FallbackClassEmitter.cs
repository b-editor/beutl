using System.Collections.Immutable;
using System.Text;

using Beutl.Engine.SourceGenerators.Analysis;

using Microsoft.CodeAnalysis;

namespace Beutl.Engine.SourceGenerators.Emit;

public static class FallbackClassEmitter
{
    public static string Emit(FallbackClassInfo info)
    {
        INamedTypeSymbol symbol = info.Symbol;
        string? namespaceName = symbol.ContainingNamespace is { IsGlobalNamespace: false } ns
            ? ns.ToDisplayString()
            : null;

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (namespaceName is not null)
        {
            sb.AppendLine($"namespace {namespaceName};");
            sb.AppendLine();
        }

        sb.AppendLine($"partial class {symbol.Name} : global::Beutl.Serialization.IFallback");
        sb.AppendLine("{");

        string indent = "    ";

        // Json property
        EmitJsonProperty(sb, indent);
        sb.AppendLine();

        // Serialize override
        EmitSerialize(sb, indent);
        sb.AppendLine();

        // Deserialize override
        EmitDeserialize(sb, indent);
        sb.AppendLine();

        // TryGetTypeName
        EmitTryGetTypeName(sb, indent);

        // Abstract method stubs
        foreach (AbstractMethodInfo methodInfo in info.AbstractMethods)
        {
            sb.AppendLine();
            EmitAbstractMethodStub(sb, indent, methodInfo);
        }

        // Resource abstract method stubs
        if (info.ResourceAbstractMethods.Length > 0)
        {
            sb.AppendLine();
            EmitResourceAbstractMethods(sb, indent, info.ResourceAbstractMethods);
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitJsonProperty(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}public global::System.Text.Json.Nodes.JsonObject? Json {{ get; set; }}");
        sb.AppendLine();
        sb.AppendLine($"{indent}public global::Beutl.Serialization.FallbackReason Reason {{ get; set; }}");
        sb.AppendLine();
        sb.AppendLine($"{indent}public string? ErrorMessage {{ get; set; }}");
    }

    private static void EmitSerialize(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}public override void Serialize(global::Beutl.Serialization.ICoreSerializationContext context)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    base.Serialize(context);");
        sb.AppendLine($"{indent}    if (Json != null)");
        sb.AppendLine($"{indent}    {{");
        sb.AppendLine($"{indent}        (context as global::Beutl.Serialization.IJsonSerializationContext)?.SetJsonObject(Json);");
        sb.AppendLine($"{indent}    }}");
        sb.AppendLine($"{indent}}}");
    }

    private static void EmitDeserialize(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}public override void Deserialize(global::Beutl.Serialization.ICoreSerializationContext context)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    base.Deserialize(context);");
        sb.AppendLine($"{indent}    Json = (context as global::Beutl.Serialization.IJsonSerializationContext)?.GetJsonObject();");
        sb.AppendLine($"{indent}}}");
    }

    private static void EmitTryGetTypeName(StringBuilder sb, string indent)
    {
        sb.AppendLine($"{indent}public bool TryGetTypeName([global::System.Diagnostics.CodeAnalysis.NotNullWhen(true)] out string? result)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    result = null;");
        sb.AppendLine($"{indent}    return Json?.TryGetDiscriminator(out result) == true;");
        sb.AppendLine($"{indent}}}");
    }

    private static void EmitAbstractMethodStub(StringBuilder sb, string indent, AbstractMethodInfo methodInfo)
    {
        IMethodSymbol method = methodInfo.Method;
        string accessibility = EmitHelpers.GetAccessibility(methodInfo.DeclaredAccessibility);
        string returnType = method.ReturnsVoid
            ? "void"
            : method.ReturnType.ToDisplayString(EmitHelpers.TypeDisplayFormat);

        var paramList = new StringBuilder();
        for (int i = 0; i < method.Parameters.Length; i++)
        {
            if (i > 0)
            {
                paramList.Append(", ");
            }

            IParameterSymbol param = method.Parameters[i];
            string paramType = ResolveParameterType(param.Type, method.ContainingType);

            if (param.RefKind == RefKind.Ref)
                paramList.Append("ref ");
            else if (param.RefKind == RefKind.Out)
                paramList.Append("out ");
            else if (param.RefKind == RefKind.In)
                paramList.Append("in ");

            paramList.Append(paramType);
            paramList.Append(' ');
            paramList.Append(param.Name);
        }

        sb.AppendLine($"{indent}{accessibility} override {returnType} {method.Name}({paramList})");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    throw new global::System.InvalidOperationException();");
        sb.AppendLine($"{indent}}}");
    }

    private static void EmitResourceAbstractMethods(StringBuilder sb, string indent, ImmutableArray<AbstractMethodInfo> methods)
    {
        sb.AppendLine($"{indent}public partial class Resource");
        sb.AppendLine($"{indent}{{");

        string innerIndent = indent + "    ";
        for (int i = 0; i < methods.Length; i++)
        {
            if (i > 0)
            {
                sb.AppendLine();
            }

            EmitAbstractMethodStub(sb, innerIndent, methods[i]);
        }

        sb.AppendLine($"{indent}}}");
    }

    /// <summary>
    /// Resolves the display string for a parameter type, handling the case where
    /// a nested "Resource" type resolves to an ancestor's Resource (e.g., EngineObject.Resource)
    /// because the actual Resource type for the method's containing type is generated by another
    /// source generator and not yet visible in the semantic model.
    /// </summary>
    private static string ResolveParameterType(ITypeSymbol paramType, INamedTypeSymbol methodContainingType)
    {
        // Check if this is a nested type named "Resource" whose containing type
        // is an ancestor of the method's declaring type
        if (paramType is INamedTypeSymbol namedType
            && namedType.Name == "Resource"
            && namedType.ContainingType is INamedTypeSymbol resourceOwner
            && !SymbolEqualityComparer.Default.Equals(resourceOwner, methodContainingType))
        {
            // Verify resourceOwner is an ancestor of methodContainingType
            bool isAncestor = false;
            for (INamedTypeSymbol? current = methodContainingType.BaseType; current is not null; current = current.BaseType)
            {
                if (SymbolEqualityComparer.Default.Equals(current, resourceOwner))
                {
                    isAncestor = true;
                    break;
                }
            }

            if (isAncestor)
            {
                // The method's containing type will have its own Resource generated
                // by EngineObjectResourceGenerator, so use that type instead
                string containingTypeDisplay = methodContainingType.ToDisplayString(EmitHelpers.TypeDisplayFormat);
                return $"{containingTypeDisplay}.Resource";
            }
        }

        return paramType.ToDisplayString(EmitHelpers.TypeDisplayFormat);
    }
}
